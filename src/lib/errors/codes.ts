/**
 * Error codes for the Cascadia PLM application.
 * These codes are used to categorize errors and map them to HTTP status codes.
 */
export enum ErrorCode {
  // Authentication (1xxx)
  AUTH_REQUIRED = 'AUTH_REQUIRED',
  AUTH_INVALID_CREDENTIALS = 'AUTH_INVALID_CREDENTIALS',
  AUTH_SESSION_EXPIRED = 'AUTH_SESSION_EXPIRED',
  AUTH_ACCOUNT_LOCKED = 'AUTH_ACCOUNT_LOCKED',

  // Authorization (2xxx)
  PERMISSION_DENIED = 'PERMISSION_DENIED',
  ROLE_REQUIRED = 'ROLE_REQUIRED',
  RESOURCE_FORBIDDEN = 'RESOURCE_FORBIDDEN',

  // Validation (3xxx)
  VALIDATION_FAILED = 'VALIDATION_FAILED',
  VALIDATION_FIELD_REQUIRED = 'VALIDATION_FIELD_REQUIRED',
  VALIDATION_FIELD_INVALID = 'VALIDATION_FIELD_INVALID',
  VALIDATION_SCHEMA_MISMATCH = 'VALIDATION_SCHEMA_MISMATCH',

  // Resource (4xxx)
  RESOURCE_NOT_FOUND = 'RESOURCE_NOT_FOUND',
  RESOURCE_ALREADY_EXISTS = 'RESOURCE_ALREADY_EXISTS',
  RESOURCE_CONFLICT = 'RESOURCE_CONFLICT',
  RESOURCE_LOCKED = 'RESOURCE_LOCKED',

  // Business Logic (5xxx)
  WORKFLOW_INVALID_TRANSITION = 'WORKFLOW_INVALID_TRANSITION',
  WORKFLOW_ACTION_NOT_ALLOWED = 'WORKFLOW_ACTION_NOT_ALLOWED',
  ITEM_REVISION_CONFLICT = 'ITEM_REVISION_CONFLICT',
  ITEM_RELATIONSHIP_CYCLE = 'ITEM_RELATIONSHIP_CYCLE',
  BRANCH_PROTECTED = 'BRANCH_PROTECTED',
  MERGE_CONFLICT = 'MERGE_CONFLICT',
  FILE_TOO_LARGE = 'FILE_TOO_LARGE',
  FILE_TYPE_NOT_ALLOWED = 'FILE_TYPE_NOT_ALLOWED',
  FILE_CHECKOUT_REQUIRED = 'FILE_CHECKOUT_REQUIRED',

  // Database (6xxx)
  DB_CONNECTION_FAILED = 'DB_CONNECTION_FAILED',
  DB_QUERY_FAILED = 'DB_QUERY_FAILED',
  DB_TRANSACTION_FAILED = 'DB_TRANSACTION_FAILED',
  DB_CONSTRAINT_VIOLATION = 'DB_CONSTRAINT_VIOLATION',

  // External Services (7xxx)
  EXTERNAL_SERVICE_UNAVAILABLE = 'EXTERNAL_SERVICE_UNAVAILABLE',
  EXTERNAL_SERVICE_TIMEOUT = 'EXTERNAL_SERVICE_TIMEOUT',
  EXTERNAL_SERVICE_ERROR = 'EXTERNAL_SERVICE_ERROR',

  // System (8xxx)
  INTERNAL_ERROR = 'INTERNAL_ERROR',
  NOT_IMPLEMENTED = 'NOT_IMPLEMENTED',
  RATE_LIMITED = 'RATE_LIMITED',

  // Jobs (9xxx)
  JOB_NOT_FOUND = 'JOB_NOT_FOUND',
  JOB_TYPE_NOT_FOUND = 'JOB_TYPE_NOT_FOUND',
  JOB_ALREADY_COMPLETED = 'JOB_ALREADY_COMPLETED',
  JOB_QUEUE_FAILED = 'JOB_QUEUE_FAILED',
  JOB_HANDLER_NOT_FOUND = 'JOB_HANDLER_NOT_FOUND',
}

